import {defineField, defineType} from 'sanity'
import {MediaWithAIButton} from '../components/inputs/MediaWithAIButton'
import {TagReferenceInput} from '../components/inputs/TagReferenceInput'
import {VectorStoreStatus} from '../components/status/VectorStoreStatus'

export const visualEssayImage = defineType({
  name: 'visualEssayImage',
  title: 'Visual Essay Image',
  type: 'document',
  fields: [
    defineField({
      name: 'image',
      title: 'Image',
      type: 'image',
      options: {
        hotspot: true
      },
      validation: (Rule) => Rule.required(),
      components: {
        input: MediaWithAIButton,
      },
    }),
    defineField({
      name: 'vectorStoreStatus',
      title: 'Vector Store',
      type: 'string',
      readOnly: true,
      components: {
        input: VectorStoreStatus,
      },
    }),
    defineField({
      name: 'aiDescription',
      title: 'Description',
      type: 'text',
      description: 'Description generated by AI from the image',
    }),
    defineField({
      name: 'aiMoodTags',
      title: 'AI Mood Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      description: 'Mood tags generated by AI from the image',
      components: {
        input: TagReferenceInput,
      },
    }),
    defineField({
      name: 'metadata',
      title: 'Metadata',
      type: 'archiveMetadata',
    }),
  ],
  preview: {
    select: {
      title: 'metadata.fileName',
      subtitle: 'metadata.artName',
      media: 'image',
    },
  },
})
