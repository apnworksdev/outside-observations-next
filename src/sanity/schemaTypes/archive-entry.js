import {defineField, defineType, useFormValue} from 'sanity'
import React from 'react'
import {ImageWithAIButton} from '../components/inputs/ImageWithAIButton'
import {TagReferenceInput} from '../components/inputs/TagReferenceInput'
import {VectorStoreStatus} from '../components/status/VectorStoreStatus'
import {aiAutoFillAction} from '../actions/aiAutoFillAction'

export const archiveEntry = defineType({
  name: 'archiveEntry',
  title: 'Archive',
  preview: {
    select: {
      title: 'artName',
    },
  },
  type: 'document',
  actions: (previous) => [...previous, aiAutoFillAction],
  fields: [
    defineField({
      name: 'poster',
      title: 'Poster',
      type: 'image',
      options: {
        hotspot: true
      },
      components: {
        input: ImageWithAIButton
      }
    }),
    defineField({
      name: 'vectorStoreStatus',
      title: 'Vector Store',
      type: 'string',
      readOnly: true,
      components: {
        input: VectorStoreStatus,
      },
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'artName',
        maxLength: 96,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'artName',
      title: 'Art Name',
      type: 'string',
    }),
    defineField({
      name: 'fileName',
      title: 'File Name',
      type: 'string',
    }),
    defineField({
      name: 'source',
      title: 'Source/Author',
      type: 'string',
    }),
    defineField({
      name: 'year',
      title: 'Year',
      type: 'number',
    }),
    defineField({
      name: 'aiDescription',
      title: 'Description',
      type: 'text',
      description: 'Description generated by AI from the image',
    }),
    defineField({
      name: 'tags',
      title: 'Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      components: {
        input: TagReferenceInput,
      },
    }),
    defineField({
      name: 'aiMoodTags',
      title: 'AI Mood Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      description: 'Mood tags generated by AI from the image',
      components: {
        input: TagReferenceInput,
      },
    }),
  ],
})