import {defineField, defineType} from 'sanity'
import {MediaWithAIButton} from '../components/inputs/MediaWithAIButton'
import {TagReferenceInput} from '../components/inputs/TagReferenceInput'
import {VisualEssayImagesInput} from '../components/inputs/VisualEssayImagesInput'
import {VectorStoreStatus} from '../components/status/VectorStoreStatus'
import {aiAutoFillAction} from '../actions/aiAutoFillAction'

export const archiveEntry = defineType({
  name: 'archiveEntry',
  title: 'Archive',
  preview: {
    select: {
      title: 'metadata.artName',
    },
  },
  type: 'document',
  actions: (previous) => [...previous, aiAutoFillAction],
  fields: [
    defineField({
      name: 'mediaType',
      title: 'Media Type',
      type: 'string',
      options: {
        list: [
          {title: 'Image', value: 'image'},
          {title: 'Video', value: 'video'},
          {title: 'Text', value: 'text'},
          {title: 'Visual Essay', value: 'visualEssay'},
        ],
        layout: 'radio',
      },
      initialValue: 'image',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'video',
      title: 'Video',
      type: 'file',
      options: {
        accept: 'video/*',
      },
      hidden: ({parent}) => parent?.mediaType !== 'video',
      components: {
        input: MediaWithAIButton
      },
    }),
    defineField({
      name: 'vimeoUrl',
      title: 'Vimeo link',
      type: 'string',
      description: 'Optional Vimeo video URL. When set, the frontend will use this instead of the uploaded video file.',
      hidden: ({parent}) => parent?.mediaType !== 'video',
    }),
    defineField({
      name: 'poster',
      title: 'Poster',
      type: 'image',
      options: {
        hotspot: true
      },
      description: 'For videos, please upload a poster image with the same dimensions as the video.',
      components: {
        input: MediaWithAIButton
      },
      hidden: ({parent}) => parent?.mediaType === 'visualEssay',
    }),
    defineField({
      name: 'textContent',
      title: 'Text Content',
      type: 'text',
      hidden: ({parent}) => parent?.mediaType !== 'text',
    }),
    defineField({
      name: 'pdf',
      title: 'PDF',
      type: 'file',
      options: {
        accept: 'application/pdf',
      },
      hidden: ({parent}) => parent?.mediaType !== 'visualEssay',
      components: {
        input: MediaWithAIButton
      },
    }),
    defineField({
      name: 'textMarkup',
      title: 'Text Markup',
      type: 'array',
      of: [
        {
          type: 'block',
          styles: [
            {title: 'Title', value: 'h2'},
            {title: 'Normal', value: 'normal'},
          ],
        },
      ],
      hidden: ({parent}) => parent?.mediaType !== 'visualEssay',
    }),
    defineField({
      name: 'visualEssayImages',
      title: 'Visual Essay Images',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'visualEssayImage'}],
          weak: true,
        },
      ],
      hidden: ({parent}) => parent?.mediaType !== 'visualEssay',
      components: {
        input: VisualEssayImagesInput,
      },
    }),
    defineField({
      name: 'vectorStoreStatus',
      title: 'Vector Store',
      type: 'string',
      readOnly: true,
      components: {
        input: VectorStoreStatus,
      },
    }),
    defineField({
      name: 'metadata',
      title: 'Metadata',
      type: 'archiveMetadata',
    }),
    defineField({
      name: 'aiDescription',
      title: 'Description',
      type: 'text',
      description: 'Description generated by AI from the image or video',
    }),
    defineField({
      name: 'aiMoodTags',
      title: 'AI Mood Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      description: 'Mood tags generated by AI from the image or video',
      components: {
        input: TagReferenceInput,
      },
    }),
  ],
})