import {defineField, defineType, useFormValue} from 'sanity'
import React from 'react'
import {MediaWithAIButton} from '../components/inputs/MediaWithAIButton'
import {TagReferenceInput} from '../components/inputs/TagReferenceInput'
import {VectorStoreStatus} from '../components/status/VectorStoreStatus'
import {aiAutoFillAction} from '../actions/aiAutoFillAction'

export const archiveEntry = defineType({
  name: 'archiveEntry',
  title: 'Archive',
  preview: {
    select: {
      title: 'artName',
    },
  },
  type: 'document',
  actions: (previous) => [...previous, aiAutoFillAction],
  fields: [
    defineField({
      name: 'mediaType',
      title: 'Media Type',
      type: 'string',
      options: {
        list: [
          {title: 'Image', value: 'image'},
          {title: 'Video', value: 'video'},
          {title: 'Text', value: 'text'},
        ],
        layout: 'radio',
      },
      initialValue: 'image',
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'video',
      title: 'Video',
      type: 'file',
      options: {
        accept: 'video/*',
      },
      hidden: ({parent}) => parent?.mediaType !== 'video',
      components: {
        input: MediaWithAIButton
      },
    }),
    defineField({
      name: 'poster',
      title: 'Poster',
      type: 'image',
      options: {
        hotspot: true
      },
      description: 'For videos, please upload a poster image with the same dimensions as the video.',
      components: {
        input: MediaWithAIButton
      },
    }),
    defineField({
      name: 'textContent',
      title: 'Text Content',
      type: 'text',
      hidden: ({parent}) => parent?.mediaType !== 'text',
    }),
    defineField({
      name: 'vectorStoreStatus',
      title: 'Vector Store',
      type: 'string',
      readOnly: true,
      components: {
        input: VectorStoreStatus,
      },
    }),
    defineField({
      name: 'slug',
      title: 'Slug',
      type: 'slug',
      options: {
        source: 'artName',
        maxLength: 96,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: 'artName',
      title: 'Art Name',
      type: 'string',
    }),
    defineField({
      name: 'fileName',
      title: 'File Name',
      type: 'string',
    }),
    defineField({
      name: 'source',
      title: 'Source/Author',
      type: 'string',
    }),
    defineField({
      name: 'year',
      title: 'Year',
      type: 'number',
    }),
    defineField({
      name: 'aiDescription',
      title: 'Description',
      type: 'text',
      description: 'Description generated by AI from the image or video',
    }),
    defineField({
      name: 'tags',
      title: 'Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      components: {
        input: TagReferenceInput,
      },
    }),
    defineField({
      name: 'aiMoodTags',
      title: 'AI Mood Tags',
      type: 'array',
      of: [
        {
          type: 'reference',
          to: [{type: 'tag'}],
        },
      ],
      description: 'Mood tags generated by AI from the image or video',
      components: {
        input: TagReferenceInput,
      },
    }),
  ],
})